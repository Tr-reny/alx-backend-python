#!/usr/bin/env python3
"""
This module contains a function 'floor' that
takes a float as argument and returns the floor of the float.
"""

import math

def floor(n: float) -> int:
    """
    Returns the floor of a float.

    Args:
        n: the float to floor

    Returns:
        The floor of n as an integer.
    """
    return math.floor(n)

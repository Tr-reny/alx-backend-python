#!/usr/bin/env python3
"""
This module contains a function 'concat' that
takes two strings as arguments and returns their concatenation.
"""

def concat(str1: str, str2: str) -> str:
    """
    Returns the concatenation of two strings.

    Args:
        str1: the first string
        str2: the second string

    Returns:
        The concatenation of str1 and str2 as a string.
    """
    return str1 + str2

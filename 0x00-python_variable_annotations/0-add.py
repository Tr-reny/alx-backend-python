#!/usr/bin/env python3
"""
This module contains a function 'add' that
takes two floats as arguments and returns their sum.
"""

def add(a: float, b: float) -> float:
    """
    Returns the sum of two floats.

    Args:
        a: the first float
        b: the second float

    Returns:
        The sum of a and b as a float.
    """
    return a + b
